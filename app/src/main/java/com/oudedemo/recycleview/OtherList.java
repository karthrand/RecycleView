package com.oudedemo.recycleview;

public class OtherList
{
    private String name;
    private int imageId;
    public OtherList(String name, int imageId){
        this.name = name;
        this.imageId = imageId;
    }
    public String getName(){
        return name;
    }
    public int getImageId(){
        return imageId;
    }
}
